public class OperadorLogico {
    public static void main(String[] args) {
        /* Operador Ternario
        int n1, n2, maior;
        n1 = 4;
        n2 = 20;

        maior = (n1 > n2) ? 0:1;
        System.out.println(maior);

        Operadores Logicos*/
        byte x, y, z;
        x = 4;
        y = 5;
        z = 8;

        boolean r;
        r  = (x < y || y > z) ? true: false;
        System.out.println(r);

    }
}
